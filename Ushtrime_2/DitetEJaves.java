public class DitetEJaves {
    public static void main(String[] args) {
        short ditaEJaves = 6;

        if (ditaEJaves > 0 && ditaEJaves < 5)
        {
            System.out.print("Ditë pune");
        }
        else if (ditaEJaves > 5 && ditaEJaves <= 7) 
        {
            System.out.print("Vikend");
        }
        else
        {
            System.out.print("Dita e dhënë është gabim");
        }
    }
}
